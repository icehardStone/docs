#!/bin/bash

# ls 命令
# 其功能是列出指定目录下的内容及其相关的属性信息

ls --help

ls -a                               #显示所有的文件及其目录（包括隐藏开头的）
ls -l                               #使用长格式列出文件及其目录信息
ls -r                               #使用长格式列出文件及其目录信息
ls -t                               #根据最后的修改时间排序
ls -S                              #根据文件大小排序
ls -R                              #第归列出所有子目录